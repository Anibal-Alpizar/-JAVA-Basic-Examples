
public class Declaraciones_Operadores {

	public static void main(String[] args) {
		
		
		int a=5;	//Constante a, numero fijo
		
		
		
		int b;		//Declaracion de la variable b
		b=7;		//Iniciacion de la variable b
		
		
		int c=b+a;	//Opercion a ejecutar
		
		
		
		c+=1;		//Ingremento de la variable c
		c-=6;		//Desincremento de la variable c
		
		
		System.out.print(c);
	}

}
